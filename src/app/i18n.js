const EN_DYNAMIC_MESSAGES = [
  [/^已将「(.+)」移出平铺$/, ([, name]) => `Removed “${name}” from the grid`],
  [/^已将「(.+)」加入平铺$/, ([, name]) => `Added “${name}” to the grid`],
  [/^(.+) 已加入对话$/, ([, name]) => `${name} added to chat`],
  [/^已切换至 (.+)$/, ([, model]) => `Switched to ${model}`],
  [/^工作目录已切换至 (.+)$/, ([, workspace]) => `Working directory changed to ${workspace}`],
  [/^权限模式已切换为(.+)$/, ([, mode]) => `Permission mode changed to ${EN_MESSAGES[mode] || EN_EXTENDED_MESSAGES[mode] || mode}`],
  [/^测试通知已发送到 (\d+) 个会话$/, ([, count]) => `Test notification sent to ${count} chats`],
  [/^(.+) 已(启用|停用)$/, ([, name, state]) => `${name} ${state === '启用' ? 'enabled' : 'disabled'}`],
  [/^(.+)已建立双向连接$/, ([, name]) => `${EN_MESSAGES[name] || EN_EXTENDED_MESSAGES[name] || name} two-way connection established`],
  [/^(.+)已重新连接$/, ([, name]) => `${EN_MESSAGES[name] || EN_EXTENDED_MESSAGES[name] || name} reconnected`],
  [/^(.+)已解除连接$/, ([, name]) => `${EN_MESSAGES[name] || EN_EXTENDED_MESSAGES[name] || name} disconnected`],
  [/^(.+)成功$/, ([, action]) => `${EN_MESSAGES[action] || EN_EXTENDED_MESSAGES[action] || action} succeeded`],
]

export function translateText(message, language = DEFAULT_LANGUAGE, values) {
  if (typeof message !== 'string') return message
  const translated = i18n.getFixedT(language)(message, values)
  if (language !== 'en-US' || Object.hasOwn(EN_TRANSLATIONS, message)) return translated
  for (const [pattern, replace] of EN_DYNAMIC_MESSAGES) {
    const match = translated.match(pattern)
    if (match) return replace(match)
  }
  return translated
}

export function storedLanguage() {
  try {
    const stored = localStorage.getItem(STORAGE_KEYS.language)
    return SUPPORTED_LANGUAGES.includes(stored) ? stored : DEFAULT_LANGUAGE
  } catch {
    return DEFAULT_LANGUAGE
  }
}

const EN_TRANSLATIONS = Object.freeze({ ...EN_MESSAGES, ...EN_EXTENDED_MESSAGES })

